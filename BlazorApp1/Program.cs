using BlazorApp1.Components;
using BlazorApp1.Infrastructure.Data;
using BlazorApp1.Services;
using BlazorApp1.Services.Interfaces;
using Microsoft.EntityFrameworkCore;
using Npgsql;

namespace BlazorApp1;

/// <summary>
/// Diem vao khoi dong ung dung Blazor Server va khoi tao ha tang du lieu.
/// </summary>
public sealed class Program
{
    /// <summary>
    /// Cau hinh DI, pipeline va chay web host.
    /// </summary>
    public static async Task Main(string[] args)
    {
        var builder = WebApplication.CreateBuilder(args);

        builder.Services
            .AddRazorComponents()
            .AddInteractiveServerComponents();

        var connectionString = builder.Configuration.GetConnectionString("DefaultConnection");
        if (string.IsNullOrWhiteSpace(connectionString))
        {
            throw new InvalidOperationException("Missing connection string 'DefaultConnection'.");
        }

        // Cau hinh PostgreSQL cho ung dung Blazor + EF Core.
        builder.Services.AddDbContextFactory<AppDbContext>(options =>
            options.UseNpgsql(connectionString));

        builder.Services.AddScoped<IDonViTinhService, DonViTinhService>();
        builder.Services.AddScoped<ILoaiSanPhamService, LoaiSanPhamService>();
        builder.Services.AddScoped<ISanPhamService, SanPhamService>();
        builder.Services.AddScoped<INhaCungCapService, NhaCungCapService>();
        builder.Services.AddScoped<IKhoService, KhoService>();

        var app = builder.Build();

        if (!app.Environment.IsDevelopment())
        {
            app.UseExceptionHandler("/Error", createScopeForErrors: true);
            app.UseHsts();
        }

        app.UseHttpsRedirection();
        app.UseStaticFiles();
        app.UseAntiforgery();

        app.MapRazorComponents<App>()
            .AddInteractiveServerRenderMode();

        var autoMigrate = builder.Configuration.GetValue<bool>("Database:AutoMigrateOnStartup");
        await InitializeDatabaseAsync(app, autoMigrate);
        await app.RunAsync();
    }

    /// <summary>
    /// Ap migration de tao/cap nhat schema khi khoi dong.
    /// </summary>
    private static async Task InitializeDatabaseAsync(WebApplication app, bool autoMigrate)
    {
        await using var scope = app.Services.CreateAsyncScope();
        var logger = scope.ServiceProvider
            .GetRequiredService<ILoggerFactory>()
            .CreateLogger<Program>();
        var dbFactory = scope.ServiceProvider.GetRequiredService<IDbContextFactory<AppDbContext>>();
        await using var dbContext = await dbFactory.CreateDbContextAsync();

        try
        {
            if (autoMigrate)
            {
                await dbContext.Database.MigrateAsync();
            }

            await EnsureAssignmentTablesAsync(dbContext);
        }
        catch (PostgresException ex) when (ex.SqlState == "28P01")
        {
            logger.LogError(ex, "Sai thong tin dang nhap PostgreSQL (username/password).");
            throw new InvalidOperationException(
                "PostgreSQL authentication failed for 'ConnectionStrings:DefaultConnection'. " +
                "Vui long kiem tra Username/Password trong appsettings hoac User Secrets.",
                ex);
        }
        catch (Exception ex)
        {
            logger.LogError(ex, "Khong the khoi tao co so du lieu cho bai 1, bai 2, bai 3, bai 4 va bai 5.");
            throw;
        }
    }

    private static async Task EnsureAssignmentTablesAsync(AppDbContext dbContext)
    {
        // Dung CREATE IF NOT EXISTS de startup an toan khi migration va script assignment cung ton tai.
        var sql = """
                  CREATE TABLE IF NOT EXISTS "tbl_DM_Don_Vi_Tinh" (
                      "Don_Vi_Tinh_ID" integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                      "Ten_Don_Vi_Tinh" varchar(100) NOT NULL,
                      "Ghi_Chu" varchar(255) NULL,
                      "Is_Active" boolean NOT NULL DEFAULT TRUE
                  );

                  ALTER TABLE "tbl_DM_Don_Vi_Tinh"
                      ADD COLUMN IF NOT EXISTS "Is_Active" boolean NOT NULL DEFAULT TRUE;

                  CREATE UNIQUE INDEX IF NOT EXISTS "UQ_tbl_DM_Don_Vi_Tinh_Ten_Don_Vi_Tinh"
                      ON "tbl_DM_Don_Vi_Tinh" ("Ten_Don_Vi_Tinh");

                  CREATE TABLE IF NOT EXISTS "tbl_DM_Loai_San_Pham" (
                      "Loai_San_Pham_ID" integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                      "Ma_LSP" varchar(50) NOT NULL,
                      "Ten_LSP" varchar(120) NOT NULL,
                      "Ghi_Chu" varchar(255) NULL,
                      "Is_Active" boolean NOT NULL DEFAULT TRUE
                  );

                  ALTER TABLE "tbl_DM_Loai_San_Pham"
                      ADD COLUMN IF NOT EXISTS "Is_Active" boolean NOT NULL DEFAULT TRUE;

                  CREATE UNIQUE INDEX IF NOT EXISTS "UQ_tbl_DM_Loai_San_Pham_Ma_LSP"
                      ON "tbl_DM_Loai_San_Pham" ("Ma_LSP");

                  CREATE UNIQUE INDEX IF NOT EXISTS "UQ_tbl_DM_Loai_San_Pham_Ten_LSP"
                      ON "tbl_DM_Loai_San_Pham" ("Ten_LSP");

                  CREATE TABLE IF NOT EXISTS "tbl_DM_San_Pham" (
                      "San_Pham_ID" integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                      "Ma_San_Pham" varchar(50) NOT NULL,
                      "Ten_San_Pham" varchar(200) NOT NULL,
                      "Loai_San_Pham_ID" integer NOT NULL,
                      "Don_Vi_Tinh_ID" integer NOT NULL,
                      "Ghi_Chu" varchar(255) NULL,
                      "Is_Active" boolean NOT NULL DEFAULT TRUE
                  );

                  ALTER TABLE "tbl_DM_San_Pham"
                      ADD COLUMN IF NOT EXISTS "Is_Active" boolean NOT NULL DEFAULT TRUE;

                  CREATE UNIQUE INDEX IF NOT EXISTS "UQ_tbl_DM_San_Pham_Ma_San_Pham"
                      ON "tbl_DM_San_Pham" ("Ma_San_Pham");

                  CREATE INDEX IF NOT EXISTS "IX_tbl_DM_San_Pham_Loai_San_Pham_ID"
                      ON "tbl_DM_San_Pham" ("Loai_San_Pham_ID");

                  CREATE INDEX IF NOT EXISTS "IX_tbl_DM_San_Pham_Don_Vi_Tinh_ID"
                      ON "tbl_DM_San_Pham" ("Don_Vi_Tinh_ID");

                  DO $$
                  BEGIN
                      IF NOT EXISTS (
                          SELECT 1
                          FROM pg_constraint
                          WHERE conname = 'FK_tbl_DM_San_Pham_tbl_DM_Loai_San_Pham_Loai_San_Pham_ID'
                      ) THEN
                          ALTER TABLE "tbl_DM_San_Pham"
                              ADD CONSTRAINT "FK_tbl_DM_San_Pham_tbl_DM_Loai_San_Pham_Loai_San_Pham_ID"
                              FOREIGN KEY ("Loai_San_Pham_ID")
                              REFERENCES "tbl_DM_Loai_San_Pham" ("Loai_San_Pham_ID")
                              ON DELETE RESTRICT;
                      END IF;

                      IF NOT EXISTS (
                          SELECT 1
                          FROM pg_constraint
                          WHERE conname = 'FK_tbl_DM_San_Pham_tbl_DM_Don_Vi_Tinh_Don_Vi_Tinh_ID'
                      ) THEN
                          ALTER TABLE "tbl_DM_San_Pham"
                              ADD CONSTRAINT "FK_tbl_DM_San_Pham_tbl_DM_Don_Vi_Tinh_Don_Vi_Tinh_ID"
                              FOREIGN KEY ("Don_Vi_Tinh_ID")
                              REFERENCES "tbl_DM_Don_Vi_Tinh" ("Don_Vi_Tinh_ID")
                              ON DELETE RESTRICT;
                      END IF;
                  END $$;

                  CREATE TABLE IF NOT EXISTS "tbl_DM_NCC" (
                      "NCC_ID" integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                      "Ma_NCC" varchar(50) NOT NULL,
                      "Ten_NCC" varchar(150) NOT NULL,
                      "Ghi_Chu" varchar(255) NULL,
                      "Is_Active" boolean NOT NULL DEFAULT TRUE
                  );

                  ALTER TABLE "tbl_DM_NCC"
                      ADD COLUMN IF NOT EXISTS "Is_Active" boolean NOT NULL DEFAULT TRUE;

                  CREATE UNIQUE INDEX IF NOT EXISTS "UQ_tbl_DM_NCC_Ma_NCC"
                      ON "tbl_DM_NCC" ("Ma_NCC");

                  CREATE UNIQUE INDEX IF NOT EXISTS "UQ_tbl_DM_NCC_Ten_NCC"
                      ON "tbl_DM_NCC" ("Ten_NCC");

                  CREATE TABLE IF NOT EXISTS "tbl_DM_Kho" (
                      "Kho_ID" integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                      "Ten_Kho" varchar(150) NOT NULL,
                      "Ghi_Chu" varchar(255) NULL,
                      "Is_Active" boolean NOT NULL DEFAULT TRUE
                  );

                  ALTER TABLE "tbl_DM_Kho"
                      ADD COLUMN IF NOT EXISTS "Is_Active" boolean NOT NULL DEFAULT TRUE;

                  CREATE UNIQUE INDEX IF NOT EXISTS "UQ_tbl_DM_Kho_Ten_Kho"
                      ON "tbl_DM_Kho" ("Ten_Kho");
                  """;

        await dbContext.Database.ExecuteSqlRawAsync(sql);
    }
}
