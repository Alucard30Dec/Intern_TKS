using BlazorApp1.Components;
using BlazorApp1.Infrastructure.Data;
using BlazorApp1.Services;
using BlazorApp1.Services.Interfaces;
using Microsoft.EntityFrameworkCore;
using Npgsql;

namespace BlazorApp1;

/// <summary>
/// Diem vao khoi dong ung dung Blazor Server va khoi tao ha tang du lieu.
/// </summary>
public sealed class Program
{
    /// <summary>
    /// Cau hinh DI, pipeline va chay web host.
    /// </summary>
    public static async Task Main(string[] args)
    {
        var builder = WebApplication.CreateBuilder(args);

        builder.Services
            .AddRazorComponents()
            .AddInteractiveServerComponents();

        var connectionString = builder.Configuration.GetConnectionString("DefaultConnection");
        if (string.IsNullOrWhiteSpace(connectionString))
        {
            throw new InvalidOperationException("Missing connection string 'DefaultConnection'.");
        }

        // Cau hinh PostgreSQL cho ung dung Blazor + EF Core.
        builder.Services.AddDbContextFactory<AppDbContext>(options =>
            options.UseNpgsql(connectionString));

        builder.Services.AddScoped<IDonViTinhService, DonViTinhService>();
        builder.Services.AddScoped<ILoaiSanPhamService, LoaiSanPhamService>();

        var app = builder.Build();

        if (!app.Environment.IsDevelopment())
        {
            app.UseExceptionHandler("/Error", createScopeForErrors: true);
            app.UseHsts();
        }

        app.UseHttpsRedirection();
        app.UseStaticFiles();
        app.UseAntiforgery();

        app.MapRazorComponents<App>()
            .AddInteractiveServerRenderMode();

        var autoMigrate = builder.Configuration.GetValue<bool>("Database:AutoMigrateOnStartup");
        await InitializeDatabaseAsync(app, autoMigrate);

        await app.RunAsync();
    }

    /// <summary>
    /// Ap migration de tao/cap nhat schema khi khoi dong.
    /// </summary>
    private static async Task InitializeDatabaseAsync(WebApplication app, bool autoMigrate)
    {
        await using var scope = app.Services.CreateAsyncScope();
        var logger = scope.ServiceProvider
            .GetRequiredService<ILoggerFactory>()
            .CreateLogger<Program>();
        var dbFactory = scope.ServiceProvider.GetRequiredService<IDbContextFactory<AppDbContext>>();
        await using var dbContext = await dbFactory.CreateDbContextAsync();

        try
        {
            if (autoMigrate)
            {
                await dbContext.Database.MigrateAsync();
            }

            await EnsureAssignmentTablesAsync(dbContext);
        }
        catch (PostgresException ex) when (ex.SqlState == "28P01")
        {
            logger.LogError(ex, "Sai thong tin dang nhap PostgreSQL (username/password).");
            throw new InvalidOperationException(
                "PostgreSQL authentication failed for 'ConnectionStrings:DefaultConnection'. " +
                "Vui long kiem tra Username/Password trong appsettings hoac User Secrets.",
                ex);
        }
        catch (Exception ex)
        {
            logger.LogError(ex, "Khong the khoi tao co so du lieu cho bai 1.");
            throw;
        }
    }

    private static async Task EnsureAssignmentTablesAsync(AppDbContext dbContext)
    {
        // Dung CREATE IF NOT EXISTS de startup an toan khi migration va script assignment cung ton tai.
        var sql = """
                  CREATE TABLE IF NOT EXISTS tbl_dm_don_vi_tinh (
                      don_vi_tinh_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                      ten_don_vi_tinh varchar(100) NOT NULL,
                      ghi_chu varchar(255) NULL
                  );

                  CREATE UNIQUE INDEX IF NOT EXISTS uq_tbl_dm_don_vi_tinh_ten
                      ON tbl_dm_don_vi_tinh (ten_don_vi_tinh);

                  CREATE TABLE IF NOT EXISTS tbl_dm_loai_san_pham (
                      loai_san_pham_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                      ma_lsp varchar(50) NOT NULL,
                      ten_lsp varchar(120) NOT NULL,
                      ghi_chu varchar(255) NULL
                  );

                  CREATE UNIQUE INDEX IF NOT EXISTS uq_tbl_dm_loai_san_pham_ma
                      ON tbl_dm_loai_san_pham (ma_lsp);

                  CREATE UNIQUE INDEX IF NOT EXISTS uq_tbl_dm_loai_san_pham_ten
                      ON tbl_dm_loai_san_pham (ten_lsp);
                  """;

        await dbContext.Database.ExecuteSqlRawAsync(sql);
    }
}
